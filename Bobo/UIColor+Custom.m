#import "UIColor+Custom.h"

@implementation UIColor (Custom)

+(UIColor *)mintCream
{
    return [UIColor colorWithRed:245.0/255 green:255.0/255 blue:250.0/255 alpha:1.0];
}

+(UIColor *)honeydew
{
    return [UIColor colorWithRed:240.0/255 green:255.0/255 blue:240.0/255 alpha:1.0];
}

+(UIColor *)lightSnow
{
    return [UIColor colorWithRed:255.0/255 green:250.0/255 blue:250.0/255 alpha:1.0];
}

+(UIColor *)customGray
{
    return [UIColor colorWithRed:238.0/255 green:238.0/255 blue:224.0/255 alpha:1.0];
}

@end
